package ch3;

public class OperatorEx11 {

	public static void main(String[] args) {
		
		char a = 'a';
		char d = 'd';
		int x = d - a;
		
		char zero = '0';
		char two = '2';
		int y = two - zero;
		
		System.out.println("d - a = " + "\'" + x + "\'");
		System.out.println("d - a = " + "\'" + y + "\'");
	}
}
